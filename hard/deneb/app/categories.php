<div class="col-md-8">
	<h1 class="page-header">What categories are you tested?</h1>
	<div class="well">
		<p>Each category will contain about 5 challenges and they will vary in difficulty.</p>
		<p>As mentioned earlier, there are many types of skills that can be tested in a jeopardy-style CTF.</p>
		<p>We have narrowed down to the following 8 categories of skills to be tested:</p>
	</div>

	<div class="list-group">
		<a class="list-group-item">
			<h4 class="list-group-item-heading">Cryptography</h4>
			<p class="list-group-item-text">
				Participants will have to tackle various cryptographic problems such as 
				retrieving plaintext from ciphertext, identification of algorithm, etc. This skill is 
				essential, and especially so, as year 20XX is seen as the year of cryptography, 
				because many weaknesses were discovered last year in major cryptographic 
				algorithms that are used by most computer systems and organisations.
			</p>
		</a>
		<a class="list-group-item">
			<h4 class="list-group-item-heading">Forensics Investigation</h4>
			<p class="list-group-item-text">
				Participants will be tested on common forensics skills such as steganography, 
				identification of files using file headers and data carving. Cyber terrorists 
				commonly hide messages in images, and hence, it is important that cyber 
				security professionals are equipped with these skills so that they will be able to 
				detect the presence of and decipher the hidden messages.
			</p>
		</a>
		<a class="list-group-item">
			<h4 class="list-group-item-heading">Exploitation</h4>
			<p class="list-group-item-text">
				Participants will need to think from an attacker's point of view and attempt to 
				exploit the vulnerabilities present, usually caused by bugs in the backend 
				scripts. Through these challenges, participants will learn to be more cautious 
				when coding.
			</p>
		</a>
		<a class="list-group-item">
			<h4 class="list-group-item-heading">Programming</h4>
			<p class="list-group-item-text">
				Participants will be implementing pseudo-codes. Cyber security professionals 
				will need to have strong programming skills to be able to discover 
				vulnerabilities and patch them accordingly.
			</p>
		</a>
		<a class="list-group-item">
			<h4 class="list-group-item-heading">Wireless</h4>
			<p class="list-group-item-text">
				Participants will be analysing network packet captures to understand how data 
				transmitted over a network can be intercepted, decrypted and even altered.
			</p>
		</a>
		<a class="list-group-item">
			<h4 class="list-group-item-heading">Reverse engineering</h4>
			<p class="list-group-item-text">
				Participants will be presented with challenges such as bypassing login screens 
				in applications. Then, they will have to reverse the given programs and bypass 
				the restrictions imposed. 
			</p>
		</a>
		<a class="list-group-item">
			<h4 class="list-group-item-heading">Physical Security</h4>
			<p class="list-group-item-text">
				Participants will be presented with physical security challenges, such as social 
				engineering. This category is commonly not included in most CTFs. However, 
				we are convinced that there is a need to include this category after Microsoft’s 
				official blog and Twitter accounts, as well as some of their employees’ emails, 
				were hacked into recently after a successful phishing attempt by the hacktivist 
				group Syrian Electronic Army. We hope that through such challenges, 
				participants will gain a better understanding on how social engineering 
				techniques can be used to compromise an organisation and as a result, will be 
				more aware of and guard against such attempts in future. 
			</p>
		</a>
		<a class="list-group-item">
			<h4 class="list-group-item-heading">Miscellaneous</h4>
			<p class="list-group-item-text">
				Challenges not mentioned in the above categories will be listed here. For 
				example, easy trivia questions will be released to start off the competition.
			</p>
		</a>
	</div>
</div>