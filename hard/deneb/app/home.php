  <h1 class="page-header">What is ReCon?</h1>
	<div class="well">
		<p>ReCon is a fictional entry-level jeopardy style Capture the Flag competition (CTF) that will be held at Singapore Management University.</p>
		<p>
			The purpose of creating this event is to provide a platform for both security 
			enthusiasts and for those aspiring to be cyber security professionals to learn 
			more about Information Security (IS). Therefore, participants do not need to 
			have any pre-requisite knowledge nor experience to join this competition, as 
			our main aim is to encourage more individuals to be exposed to IS. However, it 
			is recommended that participants conduct their own research prior to the 
			event to equip themselves with the skills that will be tested throughout the 
			competition. 
		</p>
	</div>

	<h1 class="page-header">Why are we organising such an event?</h1>
	<div class="well">
		<p>
			Most CTFs that are organised in Singapore are usually attack-and-defend-style.
			This specific type of CTF requires teams to secure their vulnerable services and 
			systems and at the same time, intrude into the systems of other participating 
			teams using the vulnerabilities discovered and their repertoire of tools. While 
			such CTFs can greatly help in cultivating interest among youths to become cyber 
			security professionals in future, the challenges in the competition lack diversity 
			in the skills tested. 
		</p>
		<p>
			Recently, there have been several cyber-attacks in Singapore and thus, it is 
			increasingly important to nurture even more youths to become aware of the 
			various risks and threats posed by the cyberspace and ultimately, grow 
			Singapore’s pool of Infocomm Security (IS) experts to put Singapore in a better 
			stead to defend against cyber threats. It is also imperative that cyber defenders 
			of tomorrow are well equipped with a wide range of skills so that they are 
			capable of adapting and reacting to the sophistication of future threats to the IS 
			landscape. 
		</p>
		<p>
			In jeopardy-style CTFs, there are multiple challenges from various categories, 
			such as Cryptography, Forensics Investigation and Exploitation. Upon solving a 
			challenge, the team will obtain a flag. By submitting the flags obtained, teams 
			will gain points, and they will gain even more points for completing challenges 
			with a higher level of difficulty. Bonus challenges, which can be unlocked when a 
			team’s points surpasses a certain threshold, are specially designed to allow 
			participants to attempt even more challenging problems and score more points 
			for their team. When the competition ends, the team with the highest amount of 
			points wins. 
		</p>
		<p>
			As such, we feel that the best way to allow the participants to be exposed to the 
			various domains of IS and broaden their skillset is by organising a jeopardy-style 
			CTF for them, since challenges in such CTFs are flexible in nature and span 
			multiple categories of skills. 
		</p>
	</div>
